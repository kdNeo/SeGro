    private void updateMenu() {
        int icount = jTable2.getRowCount();
        int mId = MenuManager.getIdByName(cbox_menu.getSelectedItem().toString());
        if (mId == -1) {
            return;
        }
        if (DB.iud("delete from menu_has_item where menu_idmenu='" + mId + "'")) {
            boolean flag = true;
            String sql = "";
            for (int i = 0; i < icount; i++) {
                String miid = jTable2.getValueAt(i, 0).toString();
                sql = "insert into menu_has_item(menu_item_idmenu_item,menu_idmenu) values('" + miid + "','" + mId + "' )";
                flag = DB.iud(sql);
            }
            flag = DB.iud("update menu set price = '" + jTextField1.getText() + "' where idmenu='" + mId + "' limit 1");
            if (flag) {
                JOptionPane.showMessageDialog(this, "Updated successfully.", "Success!", JOptionPane.INFORMATION_MESSAGE);
                clearFields();
            }

        }

    }
