    public void fillMenus() {
        try {
            ResultSet rs = DB.search("SELECT * from menu");
            DefaultComboBoxModel dcm = new DefaultComboBoxModel();
            while (rs.next()) {
                String name = rs.getString("menuname");
                dcm.addElement(name);
            }
            cbox_menu.setModel(dcm);
        } catch (Exception e) {
            DB.processException(e);
        }
    }

