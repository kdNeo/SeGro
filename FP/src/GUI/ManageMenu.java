    private void addItemsBySelectedName() {
        String name = cbox_menu.getSelectedItem().toString();
        if (name.isEmpty()) {
            return;
        }
        String sql = "select *,mi.code as code,mi.itemname as name from menu m inner join menu_has_item mhi inner join"
                + " menu_item mi on m.idmenu = mhi.menu_idmenu and mhi.menu_item_idmenu_item = mi.idmenu_item "
                + " where m.menuname = '" + name + "' order by mi.code, mi.itemname";
        DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
        dtm.setRowCount(0);
        try (
                ResultSet rs = DB.search(sql);) {
            if (rs == null) {
                return;
            }
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("idmenu_item"));
                v.add(rs.getString("code"));
                v.add(rs.getBoolean("mi.status"));
                v.add(rs.getString("name"));
                v.add(rs.getString("mi.price"));
                jTextField1.setText(rs.getString("m.price"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            DB.processException(e);
        }
    }

