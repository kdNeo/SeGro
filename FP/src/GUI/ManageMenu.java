    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int selectedRow = tbl_items_all.getSelectedRow();
        if (selectedRow == -1) {
            return;
        }
        DefaultTableModel t = (DefaultTableModel) jTable2.getModel();

        MenuItem i = MenuItemManager.getRowById(Integer.parseInt(tbl_items_all.getValueAt(selectedRow, 0).toString()), true);
        if (i == null) {
            return;
        }
        if (doesIdExistInList(String.valueOf(i.getId()))) {
            JOptionPane.showMessageDialog(this, "This item already exists!");
            return;
        }
        Vector v = new Vector();
        v.add(i.getId());
        v.add(i.getCode());
        v.add(i.isStatus());
        v.add(i.getName());
        v.add(i.getPrice().setScale(2).toBigInteger());
        t.addRow(v);
        t = (DefaultTableModel) tbl_items_all.getModel();
        t.removeRow(selectedRow);
    }//GEN-LAST:event_jButton1ActionPerformed

    private boolean doesIdExistInList(String id) {
        int rowCount = jTable2.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            if (jTable2.getValueAt(i, 0).toString().equals(id)) {
                return true;
            }
        }
        return false;
    }
