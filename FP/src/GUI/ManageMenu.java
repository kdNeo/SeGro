    private void loadAllItems() {
        try (
                ResultSet all = MenuItemManager.getAll(true);) {
            DefaultTableModel dtm = (DefaultTableModel) tbl_items_all.getModel();
            dtm.setRowCount(0);
            while (all.next()) {
                Vector v = new Vector();
                v.add(all.getString("idmenu_item"));
                v.add(all.getString("code"));
                v.add(all.getString("itemname"));
                v.add(all.getString("price"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            DB.processException(e);
        }
    }
