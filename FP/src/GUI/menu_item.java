    private boolean validateFields() {
        if (jTextField2.getText().isEmpty()) {
            jTextField2.grabFocus();
            return false;
        }
        if (jTextField3.getText().isEmpty()) {
            jTextField3.grabFocus();
            return false;
        }
        if (jTextField4.getText().isEmpty()) {
            jTextField4.grabFocus();
            return false;
        }
        return true;
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String s = jTextField1.getText();
        if (s.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select an item first.", "No item selected!", JOptionPane.QUESTION_MESSAGE);
            return;
        }
        if (validateFields()) {
            String s1 = jTextField2.getText();
            String s2 = jTextField3.getText();
            String s3 = jTextField4.getText();

            int i = Integer.valueOf(jTextField1.getText());

            try {
                if (DB.iud("UPDATE menu_item set itemname='" + s2 + "',price='" + s3 + "',code='" + s1 + "' where idmenu_item='" + i + "'")) {
                    clearFields();
                    loadActivated();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

