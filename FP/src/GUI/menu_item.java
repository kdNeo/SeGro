    public void clearFields() {
        ((DefaultTableModel) jTable1.getModel()).setRowCount(0);
        jTextField1.setText(null);
        jTextField2.setText(null);
        jTextField3.setText(null);
        jTextField4.setText(null);
        loadCategory();
    }

    private void loadCategory() {
        try (
                ResultSet search = DB.search("select * from menu_item_category");) {
            DefaultComboBoxModel dcm = new DefaultComboBoxModel();
            while (search.next()) {
                dcm.addElement(search.getString("name"));
            }
            cb_cat.setModel(dcm);
        } catch (Exception e) {
            DB.processException(e);
        }
    }

    private void loadActivated() {
        try {
            ResultSet rs = DB.search("SELECT * from menu_item where status='" + 1 + "'");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("idmenu_item"));
                v.add(rs.getString("code"));
                v.add(rs.getString("itemname"));
                v.add(rs.getString("price"));
                v.add(rs.getBoolean("status"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            DB.processException(e);
        }
    }

