    private void addItemsToList() {
        DefaultTableModel dtm = (DefaultTableModel) jTable3.getModel();

        int selectedRow = jTable2.getSelectedRow();
        if (selectedRow != -1) {
            Vector v = new Vector();
            v.add(jTable2.getValueAt(selectedRow, 0).toString());
            v.add(jTable2.getValueAt(selectedRow, 1).toString());
            v.add(jTable2.getValueAt(selectedRow, 2).toString());
            v.add(jTable2.getValueAt(selectedRow, 3).toString());
            dtm.addRow(v);
            dtm = (DefaultTableModel) jTable2.getModel();
            dtm.removeRow(selectedRow);
        }
    }
