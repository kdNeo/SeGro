    public void add() {
        try {
            ResultSet rs = DB.search("SELECT * from menu_item ");
            DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("idmenu_item"));
                v.add(rs.getString("code"));
                v.add(rs.getString("itemname"));
                v.add(rs.getString("price"));
                v.add(rs.getBoolean("status"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
