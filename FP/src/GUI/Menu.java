    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String name = jTextField1.getText();
        String price = jTextField2.getText();
        String code = jTextField3.getText();
        if (name.isEmpty()) {
            jTextField1.grabFocus();
            return;
        }
        if (code.isEmpty()) {
            jTextField3.grabFocus();
            return;
        }
        if (price.isEmpty()) {
            jTextField2.grabFocus();
            return;
        }

        try {
            boolean flag = true;
            flag = DB.iud("start transaction");
            if (flag) {
                int mid = DB.iudReturnId("INSERT into menu(menuname,price,code,status)VALUES('" + name + "','" + price + "','" + code + "','" + 1 + "')");

                int rows = jTable3.getRowCount();

                for (int j = 0; j < rows; j++) {
                    if (flag) {
                        flag = DB.iud("INSERT into menu_has_item(menu_item_idmenu_item,menu_idmenu)VALUES('" + jTable3.getValueAt(j, 0) + "','" + mid + "')");
                    }
                }
            }
            if (flag) {
                DB.iud("commit");
                JOptionPane.showMessageDialog(this, "Added successfully.", "Success!", JOptionPane.INFORMATION_MESSAGE);
                clearAll();
            } else {
                DB.iud("rollback");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

